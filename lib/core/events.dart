    // General Monthly Events
    // ಏಕಾದಶಿ (Ekadashi - 11th tithi of both pakshas)
    if (tIdx == 10 || tIdx == 25) {
      events.add(AstroEvent(
         name: 'ಏಕಾದಶಿ ವ್ರತ',
         description: 'ಮಹಾವಿಷ್ಣುವಿನ ಆರಾಧನೆಗಾಗಿ ಉಪವಾಸ ವ್ರತ.',
         shloka: 'ಏಕಾದಶ್ಯಾಂ ನ ಭುಂಜೀತ ಪಕ್ಷಯೋರುಭಯೋರಪಿ |',
         meaning: 'ಏಕಾದಶಿ ಅರುಣೋದಯ ವ್ಯಾಪಿನಿ ಆಗಿರಬೇಕು (ಸೂರ್ಯೋದಯಕ್ಕೆ ೯೬ ನಿಮಿಷ ಮೊದಲು). ದಶಮಿ ವಿದ್ಧೆ ಆಗಿರಬಾರದು. ಸ್ಮಾರ್ತರು ಮತ್ತು ವೈಷ್ಣವರಿಗೆ ಬೇರೆ ಬೇರೆ ನಿರ್ಣಯ. ಸಂಪೂರ್ಣ ಉಪವಾಸ, ದ್ವಾದಶಿ ಪಾರಣೆ.',
