          shloka: 'ವೈಶಾಖಸ್ಯ ಸಿತೇ ಪಕ್ಷೇ ಚತುರ್ದಶ್ಯಾಂ ಮಧುಸೂದನಃ |\\nನಾರಸಿಂಹರೂಪೇಣ ಪ್ರಾದುರ್ಭೂತೋ ನಿಶಾಮುಖೇ ||',
