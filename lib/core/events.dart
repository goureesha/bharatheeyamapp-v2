          source: 'ವ್ರತ ರತ್ನಾವಳಿ',
        ));
      } else if (tIdx == 3) {
        events.add(AstroEvent(
          name: 'ವರಸಿದ್ಧಿ ವಿನಾಯಕ ಚೌತಿ (ಗಣೇಶ ಚತುರ್ಥಿ)',
