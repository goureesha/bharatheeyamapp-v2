import 'package:bharatheeyam/core/calculator.dart';

class AstroEvent {
  final String name;
  final String description;
  final String shloka;
  final String meaning;
  final String source;

  AstroEvent({
    required this.name,
    required this.description,
    required this.shloka,
    this.meaning = '',
    required this.source,
  });
}

class EventCalculator {
  /// Returns a list of events occurring on the given PanchangData.
  /// Uses raw Chandra Masa (e.g., ಚೈತ್ರ) and Tithi Index (0-29).
  /// Events are skipped during Adhika Masa — only Nija Masa events are shown.
  static List<AstroEvent> getEventsForPanchang(PanchangData p) {
    final List<AstroEvent> events = [];

    // Skip all events during Adhika Masa
    if (p.chandraMasa.contains('ಅಧಿಕ')) return events;

    final masa = p.chandraMasaRaw;
    final tIdx = p.tithiIndex; // 0=Shukla Pratipada... 14=Pournami... 29=Amavasya
    final nak = p.nakshatra; // e.g. 'ರೋಹಿಣಿ'

    // 1. ಚೈತ್ರ ಮಾಸ (Chaitra)
    if (masa == 'ಚೈತ್ರ') {
      if (tIdx == 0) {
        events.add(AstroEvent(
          name: 'ಯುಗಾದಿ ಹಬ್ಬ (ಚಾಂದ್ರಮಾನ)',
          description: 'ಹೊಸ ವರ್ಷದ ಆರಂಭ. ಬೇವು-ಬೆಲ್ಲ ಸೇವನೆ ವಿಶೇಷ.',
          shloka: 'ಶತಾಯುರ್ವಜ್ರದೇಹತ್ವಾತ್ ಸರ್ವಸಂಪತ್ಕರಂ ಪರಮ್ |\\nಸರ್ವಾರಿಷ್ಟ ವಿನಾಶಾಯ ನಿಂಬಕಂದಳ ಭಕ್ಷಣಮ್ ||',
          meaning: 'ನೂರು ವರ್ಷ ಆಯಸ್ಸು, ವಜ್ರದಂತಹ ದೇಹ, ಸಕಲ ಸಂಪತ್ತುಗಳ ಪ್ರಾಪ್ತಿ ಮತ್ತು ಎಲ್ಲ ಅರಿಷ್ಟಗಳ ನಾಶಕ್ಕಾಗಿ ಬೇವಿನ ಚಿಗುರನ್ನು ಸೇವಿಸಬೇಕು.',
          source: 'ಧರ್ಮಸಿಂಧು - ಚೈತ್ರ ಮಾಸ',
        ));
      } else if (tIdx == 8) {
        events.add(AstroEvent(
          name: 'ಶ್ರೀರಾಮ ನವಮಿ',
          description: 'ಮರ್ಯಾದಾ ಪುರುಷೋತ್ತಮ ಶ್ರೀರಾಮಚಂದ್ರನ ಜನ್ಮದಿನ. ಪಾನಕ, ಮಜ್ಜಿಗೆ, ಕೋಸಂಬರಿ ವಿತರಣೆ.',
          shloka: 'ಚೈತ್ರೇ ನವಮ್ಯಾಂ ಪ್ರಾಕ್ಪಕ್ಷೇ ದಿವಾ ಪುಣ್ಯೇ ಪುನರ್ವಸೌ |\\nಉದಯೇ ಗುರುಗೌರಾತ್ರೋಃ ಸ್ವೋಚ್ಚಸ್ಥೇಷು ಗ್ರಹೇಷು ಚ ||',
