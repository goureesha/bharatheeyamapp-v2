        source: 'ಗಣೇಶ ಪುರಾಣ',
      ));
    }
    
    // ಅಮಾವಾಸ್ಯೆ / ಹುಣ್ಣಿಮೆ
    if (tIdx == 14) {
      events.add(AstroEvent(
        name: 'ಹುಣ್ಣಿಮೆ (ಪೌರ್ಣಿಮೆ)',
        description: 'ಪೂರ್ಣ ಚಂದ್ರನ ದಿನ. ಸತ್ಯನಾರಾಯಣ ಪೂಜೆಗೆ ಪ್ರಶಸ್ತ.',
        shloka: 'ಪೌರ್ಣಮಾಸ್ಯಾಂ ಚ ಸಂಪೂಜ್ಯ ದೇವಂ ನಾರಾಯಣಂ ಪ್ರಭುಮ್ |',
        meaning: 'ಪೌರ್ಣಿಮೆಯಂದು ಪ್ರಭು ನಾರಾಯಣನನ್ನು ಪೂಜಿಸಬೇಕು.',
        source: 'ಸ್ಕಂದ ಪುರಾಣ',
      ));
    } else if (tIdx == 29) {
      events.add(AstroEvent(
        name: 'ಅಮಾವಾಸ್ಯೆ',
        description: 'ಪಿತೃ ದೇವತೆಗಳಿಗೆ ತರ್ಪಣ ನೀಡಲು ಶ್ರೇಷ್ಠ ದಿನ.',
        shloka: 'ಅಮಾವಾಸ್ಯಾಯಾಂ ಪಿತೃಭ್ಯೋ ದದ್ಯಾಚ್ಚ ತಿಲತರ್ಪಣಮ್ |',
        meaning: 'ಅಮಾವಾಸ್ಯಾ ತಿಥಿ ಅಪರಾಹ್ಣ ವ್ಯಾಪಿನಿ ಆಗಿರಬೇಕು. ಪಿತೃಗಳಿಗೆ ಎಳ್ಳು-ನೀರಿನಿಂದ ತರ್ಪಣ ನೀಡಬೇಕು. ದರ್ಶ ಶ್ರಾದ್ಧ ಮಾಡಬೇಕು.',
        source: 'ಧರ್ಮಸಿಂಧು',
      ));
    }

    return events;
  }
