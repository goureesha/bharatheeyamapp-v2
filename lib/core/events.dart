          shloka: 'ಆಷಾಢೇ ಶುಕ್ಲಪಕ್ಷೇ ತು ಹರಿಃ ಸ್ವಪಿತಿ ವೈ ಜಲೇ |',
          meaning: 'ಆಷಾಢ ಶುಕ್ಲ ಪಕ್ಷದಲ್ಲಿ ಶ್ರೀಹರಿಯು ಜಲದಲ್ಲಿ ಯೋಗನಿದ್ರೆಗೆ ಜಾರುತ್ತಾನೆ.',
          source: 'ಧರ್ಮಸಿಂಧು',
        ));
      } else if (tIdx == 14) {
        events.add(AstroEvent(
          name: 'ಗುರು ಪೌರ್ಣಿಮೆ (ವ್ಯಾಸ ಪೌರ್ಣಿಮೆ)',
          description: 'ಮಹರ್ಷಿ ವೇದವ್ಯಾಸರ ಜನ್ಮದಿನ ಹಾಗೂ ಗುರುಪೂಜೆಗೆ ಶ್ರೇಷ್ಠ ದಿನ.',
          shloka: 'ಅಜ್ಞಾನತಿಮಿರಾನ್ಧಸ್ಯ ಜ್ಞಾನಾಞ್ಜನಶಲಾಕಯಾ |\\nಚಕ್ಷುರುನ್ಮೀಲಿತಂ ಯೇನ ತಸ್ಮೈ ಶ್ರೀಗುರವೇ ನಮಃ ||',
          meaning: 'ಅಜ್ಞಾನವೆಂಬ ಅಂಧಕಾರದಲ್ಲಿ ಮುಳುಗಿದ್ದವನ ಕಣ್ಣುಗಳನ್ನು ಜ್ಞಾನವೆಂಬ ಅಂಜನಶಲಾಕೆಯಿಂದ ತೆರೆಸಿದ ಆ ಶ್ರೀಗುರುವಿಗೆ ನಮಸ್ಕಾರ.',
          source: 'ಗುರು ಗೀತಾ',
        ));
      }
    }

    // 5. ಶ್ರಾವಣ ಮಾಸ (Shravana)
    if (masa == 'ಶ್ರಾವಣ') {
      if (tIdx == 4) {
        events.add(AstroEvent(
          name: 'ನಾಗ ಪಂಚಮಿ',
          description: 'ನಾಗ ದೇವತೆಗಳ ಆರಾಧನೆ. ಹಾಲು, ತನಿ ಎರೆಯುವ ಆಚರಣೆ.',
          shloka: 'ಶ್ರಾವಣೇ ಶುಕ್ಲಪಂಚಮ್ಯಾಂ ಸ್ನಾತ್ವಾ ನಾಗಾನ್ ಪ್ರಪೂಜಯೇತ್ |',
          meaning: 'ಶ್ರಾವಣ ಶುಕ್ಲ ಪಂಚಮಿಯಂದು ಸ್ನಾನ ಮಾಡಿ ನಾಗದೇವತೆಗಳನ್ನು ಪೂಜಿಸಬೇಕು.',
          source: 'ನಿರ್ಣಯಸಿಂಧು',
        ));
      } else if (tIdx == 14) {
        events.add(AstroEvent(
          name: 'ಉಪಾಕರ್ಮ (ಋಗ್ವೇದ/ಯಜುರ್ವೇದ) / ರಕ್ಷಾ ಬಂಧನ',
          description: 'ನೂತನ ಯಜ್ಞೋಪವೀತ ಧಾರಣೆ. ಸಹೋದರ-ಸಹೋದರಿಯರ ಪ್ರೇಮದ ಸಂಕೇತ.',
          shloka: 'ಯೇನ ಬದ್ಧೋ ಬಲೀ ರಾಜಾ ದಾನವೇಂದ್ರೋ ಮಹಾಬಲಃ |\\nತೇನ ತ್ವಾಮಪಿ ಬಧ್ನಾಮಿ ರಕ್ಷೇ ಮಾ ಚಲ ಮಾ ಚಲ ||',
          meaning: 'ಯಾವ ದಾರದಿಂದ ಮಹಾಬಲಶಾಲಿ ದಾನವೇಂದ್ರ ಬಲಿ ರಾಜನು ಬದ್ಧನಾಗಿದ್ದನೋ, ಆ ದಾರದಿಂದಲೇ ನಿನ್ನನ್ನೂ ಕಟ್ಟುತ್ತೇನೆ, ರಕ್ಷೆಯೇ ಚಲಿಸಬೇಡ.',
          source: 'ಭವಿಷ್ಯ ಪುರಾಣ',
        ));
      } else if (tIdx == 22) { // Krishna Ashtami = 15 + 7 = 22
        events.add(AstroEvent(
          name: 'ಶ್ರೀ ಕೃಷ್ಣ ಜನ್ಮಾಷ್ಟಮಿ (ಗೋಕುಲಾಷ್ಟಮಿ)',
