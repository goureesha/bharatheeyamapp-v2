          shloka: 'ಜ್ಯೇಷ್ಟೇ ಮಾಸಿ ಸಿತೇ ಪಕ್ಷೇ ಪೌರ್ಣಮಾಸ್ಯಾಂ ವಿಶೇಷತಃ |\\nವಟಮೂಲೇ ಸತೀ ತ್ರೀಣಿ ದಿನಾನಿ ವ್ರತಮಾಚರೇತ್ ||',
          meaning: 'ಜ್ಯೇಷ್ಟ ಮಾಸದ ಶುಕ್ಲ ಪಕ್ಷದ ಪೌರ್ಣಿಮೆಯಂದು ವಿಶೇಷವಾಗಿ ವಟವೃಕ್ಷದ ಬುಡದಲ್ಲಿ ಸತೀಯು ಮೂರು ದಿನ ವ್ರತವನ್ನು ಆಚರಿಸಬೇಕು.',
          source: 'ನಿರ್ಣಯಸಿಂಧು',
        ));
      }
    }

    // 4. ಆಷಾಢ ಮಾಸ (Ashadha)
    if (masa == 'ಆಷಾಢ') {
      if (tIdx == 10) {
        events.add(AstroEvent(
          name: 'ಪ್ರಥಮ ಏಕಾದಶಿ (ಶಯನೀ ಏಕಾದಶಿ)',
