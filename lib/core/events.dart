          source: 'ನಿರ್ಣಯಸಿಂಧು',
        ));
      } else if (tIdx == 14) {
        events.add(AstroEvent(
          name: 'ಬುದ್ಧ ಪೌರ್ಣಿಮೆ / ಕೂರ್ಮ ಜಯಂತಿ',
          description: 'ಗೌತಮ ಬುದ್ಧನ ಮತ್ತು ವಿಷ್ಣುವಿನ ಕೂರ್ಮಾವತಾರದ ಜನ್ಮದಿನ.',
          shloka: 'ವೈಶಾಖೇ ಪೌರ್ಣಮಾಸ್ಯಾಂ ತು ಕೂರ್ಮರೂಪೀ ಜನಾರ್ದನಃ |',
