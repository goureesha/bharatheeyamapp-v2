        source: 'ಸ್ಕಂದ ಪುರಾಣ',
      ));
    }

    // ಸಂಕಷ್ಟಹರ ಚತುರ್ಥಿ (Sankashti - Krishna Chaturthi)
    if (tIdx == 18) { // 15 + 3 = 18 = Krishna Chaturthi
      events.add(AstroEvent(
        name: 'ಸಂಕಷ್ಟಹರ ಚತುರ್ಥಿ',
        description: 'ಸಕಲ ಕಷ್ಟಗಳ ನಿವಾರಣೆಗಾಗಿ ವಿಘ್ನೇಶ್ವರನಿಗೆ ಚಂದ್ರೋದಯ ಕಾಲದ ಪೂಜೆ.',
        shloka: 'ಕೃಷ್ಣಪಕ್ಷೇ ಚತುರ್ಥ್ಯಾಂ ತು ಸಂಪೂಜ್ಯ ಗಣನಾಯಕಮ್ |\\nಚಂದ್ರೋದಯೇಽರ್ಘ್ಯಂ ದದ್ಯಾಚ್ಚ ಸಂಕಷ್ಟಹರಣಾಯ ವೈ ||',
