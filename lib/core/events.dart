          source: 'ನಿರ್ಣಯಸಿಂಧು',
        ));
      } else if (tIdx == 10) {
        events.add(AstroEvent(
          name: 'ಆಮಲಕೀ ಏಕಾದಶಿ',
          description: 'ಆಮಲಕೀ (ನೆಲ್ಲಿ) ವೃಕ್ಷದ ಪೂಜೆ ಮತ್ತು ವಿಷ್ಣು ಆರಾಧನೆ.',
          shloka: 'ಫಾಲ್ಗುಣೇ ಶುಕ್ಲಪಕ್ಷೇ ಏಕಾದಶ್ಯಾಂ ಆಮಲಕೀ |\\nವೃಕ್ಷಂ ಪೂಜಯಿತ್ವಾ ವಿಷ್ಣುಂ ಸರ್ವಪಾಪಕ್ಷಯಮ್ ||',
          meaning: 'ಏಕಾದಶಿ ಅರುಣೋದಯ ವ್ಯಾಪಿನಿ. ದಶಮಿ ವಿದ್ಧೆ ವರ್ಜ್ಯ. ಆಮಲಕೀ (ನೆಲ್ಲಿ) ವೃಕ್ಷಕ್ಕೆ ಪೂಜೆ. ಸಂಪೂರ್ಣ ಉಪವಾಸ.',
          source: 'ಧರ್ಮಸಿಂಧು',
        ));
      } else if (tIdx == 14) {
        events.add(AstroEvent(
          name: 'ಕಾಮ ದಹನ / ಹೋಳಿ ಹುಣ್ಣಿಮೆ',
          description: 'ಮನ್ಮಥನ ದಹನ ಮತ್ತು ಪ್ರಹ್ಲಾದನ ರಕ್ಷಣೆ. ಬಣ್ಣಗಳ ಹಬ್ಬ.',
          shloka: 'ಫಾಲ್ಗುಣೇ ಪೌರ್ಣಮಾಸ್ಯಾಂ ತು ಹೋಲಿಕಾ ದಹನಂ ಸ್ಮೃತಮ್ |',
          meaning: 'ಫಾಲ್ಗುಣ ಪೌರ್ಣಿಮಾ ಸೂರ್ಯೋದಯ ವ್ಯಾಪಿನಿ. ಪ್ರದೋಷ ಕಾಲದಲ್ಲಿ ಹೋಲಿಕಾ ದಹನ ಮಾಡಬೇಕು. ಭದ್ರಾ ಕಾಲ ವರ್ಜ್ಯ.',
          source: 'ನಿರ್ಣಯಸಿಂಧು',
        ));
      }
