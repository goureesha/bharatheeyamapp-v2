          shloka: 'ಶ್ರಾವಣೇ ಬಹುಳೇಽಷ್ಟಮ್ಯಾಂ ರೋಹಿಣ್ಯಾಮರ್ಧರಾತ್ರಕೇ |\\nವಸುದೇವಗೃಹಾತೋ ಹಿ ದೇವಕ್ಯಾಂ ಜಾಯತೇ ಹರಿಃ ||',
