          name: 'ಕಾಮ ದಹನ / ಹೋಳಿ ಹುಣ್ಣಿಮೆ',
          description: 'ಮನ್ಮಥನ ದಹನ ಮತ್ತು ಪ್ರಹ್ಲಾದನ ರಕ್ಷಣೆ. ಬಣ್ಣಗಳ ಹಬ್ಬ.',
          shloka: 'ಫಾಲ್ಗುಣೇ ಪೌರ್ಣಮಾಸ್ಯಾಂ ತು ಹೋಲಿಕಾ ದಹನಂ ಸ್ಮೃತಮ್ |',
          meaning: 'ಫಾಲ್ಗುಣ ಪೌರ್ಣಿಮೆಯಂದು ಹೋಲಿಕಾ ದಹನವು ಸ್ಮರಿಸಲ್ಪಡುತ್ತದೆ.',
          source: 'ನಿರ್ಣಯಸಿಂಧು',
        ));
      }
    }

    // General Monthly Events
    // ಏಕಾದಶಿ (Ekadashi - 11th tithi of both pakshas)
    if (tIdx == 10 || tIdx == 25) {
      events.add(AstroEvent(
         name: 'ಏಕಾದಶಿ ವ್ರತ',
         description: 'ಮಹಾವಿಷ್ಣುವಿನ ಆರಾಧನೆಗಾಗಿ ಉಪವಾಸ ವ್ರತ.',
         shloka: 'ಏಕಾದಶ್ಯಾಂ ನ ಭುಂಜೀತ ಪಕ್ಷಯೋರುಭಯೋರಪಿ |',
         meaning: 'ಎರಡೂ ಪಕ್ಷಗಳ ಏಕಾದಶಿಯಂದು ಊಟ ಮಾಡಬಾರದು (ಉಪವಾಸ ಆಚರಿಸಬೇಕು).',
         source: 'ವೈಷ್ಣವ ಸಂಪ್ರದಾಯ',
      ));
    }

    // ಪ್ರದೋಷ (Pradosha - 13th tithi of both pakshas)
    if (tIdx == 12 || tIdx == 27) {
      events.add(AstroEvent(
        name: 'ಪ್ರದೋಷ ವ್ರತ',
        description: 'ಸೂರ್ಯಾಸ್ತಮಯ ಕಾಲದಲ್ಲಿ ಶಿವನ ಆರಾಧನೆ ಸರ್ವ ಪಾಪ ನಾಶಕ.',
        shloka: 'ತ್ರಯೋದಶ್ಯಾಂ ನಿಶಾಮುಖೇ ಪ್ರದೋಷಸಮಯೇ ಹರಮ್ |\\nಪೂಜಯೇದ್ ಭಕ್ತಿಭಾವೇನ ಸರ್ವಪಾಪ ವಿನಾಶನಮ್ ||',
        meaning: 'ತ್ರಯೋದಶಿಯ ಸೂರ್ಯಾಸ್ತಮಯ ಸಮಯದಲ್ಲಿ (ಪ್ರದೋಷ ಕಾಲ) ಭಕ್ತಿಯಿಂದ ಶಿವನನ್ನು ಪೂಜಿಸಿದರೆ ಸಕಲ ಪಾಪ ನಾಶವಾಗುತ್ತದೆ.',
        source: 'ಸ್ಕಂದ ಪುರಾಣ',
      ));
    }

    // ಸಂಕಷ್ಟಹರ ಚತುರ್ಥಿ (Sankashti - Krishna Chaturthi)
    if (tIdx == 18) { // 15 + 3 = 18 = Krishna Chaturthi
      events.add(AstroEvent(
        name: 'ಸಂಕಷ್ಟಹರ ಚತುರ್ಥಿ',
        description: 'ಸಕಲ ಕಷ್ಟಗಳ ನಿವಾರಣೆಗಾಗಿ ವಿಘ್ನೇಶ್ವರನಿಗೆ ಚಂದ್ರೋದಯ ಕಾಲದ ಪೂಜೆ.',
        shloka: 'ಕೃಷ್ಣಪಕ್ಷೇ ಚತುರ್ಥ್ಯಾಂ ತು ಸಂಪೂಜ್ಯ ಗಣನಾಯಕಮ್ |\\nಚಂದ್ರೋದಯೇಽರ್ಘ್ಯಂ ದದ್ಯಾಚ್ಚ ಸಂಕಷ್ಟಹರಣಾಯ ವೈ ||',
        meaning: 'ಕೃಷ್ಣ ಪಕ್ಷ ಚತುರ್ಥಿಯಂದು ಗಣನಾಯಕನನ್ನು ಪೂಜಿಸಿ, ಚಂದ್ರೋದಯದಲ್ಲಿ ಅರ್ಘ್ಯ ನೀಡಿದರೆ ಸಕಲ ಸಂಕಷ್ಟಗಳು ಹರಣವಾಗುತ್ತವೆ.',
        source: 'ಗಣೇಶ ಪುರಾಣ',
      ));
    }
    
    // ಅಮಾವಾಸ್ಯೆ / ಹುಣ್ಣಿಮೆ
    if (tIdx == 14) {
      events.add(AstroEvent(
        name: 'ಹುಣ್ಣಿಮೆ (ಪೌರ್ಣಿಮೆ)',
        description: 'ಪೂರ್ಣ ಚಂದ್ರನ ದಿನ. ಸತ್ಯನಾರಾಯಣ ಪೂಜೆಗೆ ಪ್ರಶಸ್ತ.',
        shloka: 'ಪೌರ್ಣಮಾಸ್ಯಾಂ ಚ ಸಂಪೂಜ್ಯ ದೇವಂ ನಾರಾಯಣಂ ಪ್ರಭುಮ್ |',
        meaning: 'ಪೌರ್ಣಿಮೆಯಂದು ಪ್ರಭು ನಾರಾಯಣನನ್ನು ಪೂಜಿಸಬೇಕು.',
        source: 'ಸ್ಕಂದ ಪುರಾಣ',
      ));
    } else if (tIdx == 29) {
      events.add(AstroEvent(
        name: 'ಅಮಾವಾಸ್ಯೆ',
        description: 'ಪಿತೃ ದೇವತೆಗಳಿಗೆ ತರ್ಪಣ ನೀಡಲು ಶ್ರೇಷ್ಠ ದಿನ.',
        shloka: 'ಅಮಾವಾಸ್ಯಾಯಾಂ ಪಿತೃಭ್ಯೋ ದದ್ಯಾಚ್ಚ ತಿಲತರ್ಪಣಮ್ |',
        meaning: 'ಅಮಾವಾಸ್ಯೆಯಂದು ಪಿತೃ ದೇವತೆಗಳಿಗೆ ಎಳ್ಳಿನೊಂದಿಗೆ ತರ್ಪಣ ನೀಡಬೇಕು.',
        source: 'ಧರ್ಮಸಿಂಧು',
      ));
    }

    return events;
  }
