          shloka: 'ಭಾದ್ರಪದ ಶುಕ್ಲ ಚತುರ್ದಶ್ಯಾಂ ಅನಂತವ್ರತಮಾಚರೇತ್ |',
          meaning: 'ಭಾದ್ರಪದ ಶುಕ್ಲ ಚತುರ್ದಶಿಯಂದು ಅನಂತ ವ್ರತವನ್ನು ಆಚರಿಸಬೇಕು.',
          source: 'ಧರ್ಮಸಿಂಧು',
        ));
      } else if (tIdx == 29) {
        events.add(AstroEvent(
