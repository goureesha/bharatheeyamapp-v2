        ));
      }
    }

    // 2. ವೈಶಾಖ ಮಾಸ (Vaishakha)
    if (masa == 'ವೈಶಾಖ') {
      if (tIdx == 2) {
        events.add(AstroEvent(
          name: 'ಅಕ್ಷಯ ತೃತೀಯಾ',
          description: 'ಅತ್ಯಂತ ಶುಭದಿನ. ಇಂದು ಮಾಡಿದ ದಾನ, ಜಪಗಳು ಅಕ್ಷಯ ಫಲ ನೀಡುತ್ತವೆ.',
